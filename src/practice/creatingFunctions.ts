function fancyPrint(firstString: string, secondstring: string): string {
    return `${firstString}********\n*********${secondstring}`;   
}

console.log(fancyPrint("Apple", "Baum"))


function kittiePaws(firstPaw: string, secondPaw: string): string{
    return `${firstPaw} \n\t ${secondPaw}`;
}
console.log(kittiePaws("black", "calico" ))